package code006;

public class Demo53Student {
    private String name;
    private int age;
    private boolean gender;

    public void setName(String str) {
        name = str;
    }

    public String getName() {
        return name;
    }

    public void setAge(int num) {
        age = num;
    }

    public int getAge() {
        return age;
    }

    public void setGender(boolean a) {
        gender = a;
    }

    public boolean isGender() {
        return gender;
    }
}
